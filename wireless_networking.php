        <p><span class="firstp"><strong>Our <em>wireless network</em> integration services and design solutions portfolio contains the latest technologies and products.</strong></span></p><br />
        <p>The wireless networking revolution is bringing fundamental changes to data networking. It frees the user from connecting the patch chord to the computer and the wall outlet to have network access thus making the network available anywhere, anytime.  Wireless networks are based on the IEEE 802.11 standards and with multiple stations communicating with radios that broadcast in either the 2.4GHz or 5GHz band.<p><br />
        <h3>RF Site Survey</h3>
        <p>Radio waves do not travel the same distance in all directions. Partition walls, glass partitions, doors, elevator shafts, people, and other obstacles offer attenuation, which cause the radio frequency (RF) radiation pattern to be irregular and unpredictable.  When considering the use of wireless LAN, it is extremely difficult to predict the propagation of radio waves and detect the presence of interfering signals without the use of specialized test equipment. A RF site survey is a good idea for the use of any wireless device because without a RF site survey, the network could end up with inadequate coverage and low performance in some pockets. Hence one should have an RF site survey performed to understand fully the behaviour of radio waves within a facility before installing any wireless devices.</p><br />
        <p>Tekfusion does an RF site survey to gather enough information and data to determine the number and placement of access points that will provide optimal wireless network coverage. That usually means the support of a minimum data rate in a given area. An RF site survey will also detect the presence of radio interference coming from other sources that could degrade the performance of the wireless LAN.  The need and complexity of an RF site survey will vary depending on the size of the facility and the work that is to be accomplished. </p><br />
        <h3>Wireless Network Design</h3>
        <p>Based on the initial RF site survey and existing network policy, our experienced wireless network designers will work with you to build a robust, scalable and secure wireless network that meets all of your requirements. The data speed, throughput, security, reliability, availability and future demands are the key factors considered while designing your wireless network.</p><br />
        <h3>Wireless Equipment Deployment</h3>
        <p>Wireless equipment deployment is planned and coordinated by our experienced technicians with the help of the RF site survey report.  The access points and antennas will be installed in the facility using the marked up drawing made after the site survey with little or no disruption to the existing network. We always aim to have no network down time while installing.  Necessary network cables and power cables will be laid to connect the access points to the main network.  After completing the installation the access points will be configured with proper security features to secure the wireless network. </p><br />
        <h3>Final Site Survey</h3>
        <p>On completion of the deployment, we will perform a final wireless site survey to ensure that the RF coverage is as per the initial network design and your requirements with regard to the coverage and throughput have been reached. With the final survey done before the handover, we will baseline the network to detect any movement in performance post deployment.</p><br />
